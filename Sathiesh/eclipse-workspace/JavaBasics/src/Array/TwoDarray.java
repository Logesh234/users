package Array;

public class TwoDarray {

	public static void main(String[] args) {
			int a[][]=new int[2][2];
			a[0][0]=12;
			a[0][1]=13;
			a[1][0]=14;
			a[1][1]=15;
			System.out.print(a[0][0]+"\t");
			System.out.println(a[0][1]);
			System.out.print(a[1][0]+"\t");
			System.out.println(a[1][1]);
		}

	}


